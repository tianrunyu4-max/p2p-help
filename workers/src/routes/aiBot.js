/**
 * POST /api/ai/chat
 * Body: { message: string, userId?: string }
 * Response: { code: 200, data: { reply: string } }
 */
router.post('/chat', async (request) => {
    const env = request.env

    // 检查 API Key 配置
    if (!env.DEEPSEEK_API_KEY) {
        console.error('[AI] DEEPSEEK_API_KEY 未配置')
        return Response.json({ code: 500, message: 'AI服务未配置，请联系管理员' }, { status: 500 })
    }

    try {
        const body = await request.json()
        const { message, userId } = body

        // 参数校验
        if (!message || typeof message !== 'string' || message.trim().length === 0) {
            return Response.json({ code: 400, message: '消息不能为空' })
        }

        const userMessage = message.trim().slice(0, 500) // 截断防注入
        const uid = userId ? String(userId).slice(0, 64) : null

        // ── 从 KV 读取对话历史 ──
        let history = []
        const kvKey = `ai_hist:${uid || 'anon'}`
        if (env.CACHE && uid) {
            try {
                const stored = await env.CACHE.get(kvKey, 'json')
                if (stored && Array.isArray(stored)) {
                    history = stored.slice(-8) // 最多 4 轮上下文
                }
            } catch (e) { /* KV 读取失败忽略 */ }
        }

        // ── 构建 DeepSeek 请求 ──
        const messages = [
            { role: 'system', content: SYSTEM_PROMPT },
            ...history,
            { role: 'user', content: userMessage }
        ]

        const dsResponse = await fetch('https://api.deepseek.com/chat/completions', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
                'Authorization': `Bearer ${env.DEEPSEEK_API_KEY}`
            },
            body: JSON.stringify({
                model: 'deepseek-chat',
                messages,
                max_tokens: 400,
                temperature: 0.7,
                stream: false
            }),
            // Cloudflare Workers 超时 25 秒
            signal: AbortSignal.timeout(25000)
        })

        if (!dsResponse.ok) {
            const errText = await dsResponse.text()
            console.error('[AI] DeepSeek API错误:', dsResponse.status, errText)
            return Response.json({ code: 500, message: 'AI暂时忙碌，请稍后再问 😊' })
        }

        const dsData = await dsResponse.json()
        const aiReply = dsData.choices?.[0]?.message?.content?.trim() || '抱歉，我暂时想不到好的回答，请联系您的推荐人 😊'

        // ── 保存对话历史（TTL 30分钟）──
        if (env.CACHE && uid) {
            try {
                const newHistory = [
                    ...history,
                    { role: 'user', content: userMessage },
                    { role: 'assistant', content: aiReply }
                ].slice(-10) // 保留最近 10 条
                await env.CACHE.put(kvKey, JSON.stringify(newHistory), { expirationTtl: 1800 })
            } catch (e) { /* KV 写入失败忽略 */ }
        }

        console.log(`[AI] userId=${uid} Q="${userMessage.slice(0, 30)}" → ${aiReply.slice(0, 50)}`)

        return Response.json({
            code: 200,
            data: { reply: aiReply }
        })

    } catch (error) {
        if (error.name === 'TimeoutError' || error.name === 'AbortError') {
            return Response.json({ code: 500, message: 'AI响应超时，请重试 ⏱️' })
        }
        console.error('[AI] chat error:', error)
        return Response.json({ code: 500, message: 'AI服务异常: ' + error.message }, { status: 500 })
    }
})

